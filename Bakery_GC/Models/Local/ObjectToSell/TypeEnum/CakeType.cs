namespace Bakery_GC.Models.Local.ObjectToSell.TypeEnum
{
    public enum CakeType
    {
        SpongeCake,          // Es. Pan di Spagna, Victoria sponge
        Genoise,             // Torta simile allo sponge, più compatta
        ChiffonCake,         // Leggerissima, con olio e albumi montati
        AngelFoodCake,       // Solo albumi, molto leggera
        PoundCake,           // Torte burrose e dense (es. quattro quarti)
        ButterCake,          // Torte classiche al burro
        Cheesecake,          // Torte a base di formaggio fresco
        MousseCake,          // A base di mousse, spesso senza cottura
        FruitCake,           // Torte alla frutta secca/candita o fresca
        Torte,               // Strati sottili e farciture ricche (es. Sacher)
        FlourlessCake,       // Torte senza farina (es. caprese)
        IceCreamCake,        // Torte con gelato
        MeringueCake,        // Es. pavlova o dacquoise
        CreamCake,           // Es. millefoglie, chantilly, diplomatiche
        ChocolateCake        // Qualsiasi torta in cui il cioccolato è protagonista
    }

}