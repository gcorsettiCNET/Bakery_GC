namespace Bakery_GC.Models.Local.ObjectToSell.TypeEnum
{
    public enum BreadType
    {
        Baguette,            // Classico pane francese allungato e croccante
        Ciabatta,            // Pane italiano rustico, mollica alveolata
        Sourdough,           // Pane a lievitazione naturale
        WholeWheat,          // Pane integrale
        Rye,                 // Pane di segale (es. pane nero tedesco)
        Multigrain,          // Con mix di cereali e semi
        Focaccia,            // Pane italiano schiacciato e unto
        Brioche,             // Pane dolce e soffice (può essere anche salato)
        Pita,                // Pane piatto mediorientale, con tasca
        Naan,                // Pane piatto indiano, cotto in forno tandoor
        Flatbread,           // Pane sottile senza lievitazione marcata
        Cornbread,           // Pane americano a base di farina di mais
        Bagel,               // Anello bollito e poi cotto, spesso usato per farcire
        Lavash,              // Pane armeno sottile e morbido
        PaneToscano,         // Pane senza sale, tipico della Toscana
        MilkBread,           // Pane al latte giapponese o cinese, molto soffice
        HamburgerBun,        // Panino tondo per hamburger
        HotDogBun            // Panino allungato per hot dog
    }
}