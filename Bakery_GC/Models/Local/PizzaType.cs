namespace Bakery_GC.Models.Local
{
    public enum PizzaType
    {
        Neapolitan,         // Classica napoletana (cornicione alto, impasto idratato)
        Roman,              // Pizza bassa e croccante (pizza tonda romana)
        Sicilian,           // Pizza alta e rettangolare (tipo sfincione)
        NewYorkStyle,       // Grande, sottile, pieghevole
        ChicagoDeepDish,    // Pizza alta, tipo torta salata
        DetroitStyle,       // Rettangolare, con bordo croccante al formaggio
        Calzone,            // Pizza chiusa ripiena
        StuffedCrust,       // Con bordo ripieno (tipicamente di formaggio)
        Flatbread,          // Tipo pinsa o pizza senza lievitazione tradizionale
        FocacciaStyle,      // Simile alla focaccia, più spessa
        Gourmet,            // Ingredienti ricercati, abbinamenti moderni
        GlutenFree,         // Con impasto senza glutine
        Vegan,              // Senza ingredienti animali
        WhitePizza,         // Senza salsa di pomodoro
        Margherita,         // Classico con pomodoro, mozzarella, basilico
        Pepperoni,          // Classico americano con salame piccante
        FourCheese          // Con mix di formaggi (es. gorgonzola, fontina, ecc.)
    }

}